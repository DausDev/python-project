# Arithmatic Operators

angka_pertama= int(input (" Masukkan angka pertama :"))
angka_kedua= int(input (" Masukkan angka kedua :"))
x = angka_pertama
y = angka_kedua


print ("tambah =" , (x + y))
print ("darab =", (x * y))
print ("bahagi =",(x / y))
print ("tolak =",(x - y))
print ("modulus =", (x % y))
print ("kuasa =", (x**y))
print ("floor division =", (x//y))
print ("increament =", (x + 1))
print ("decrement =", (x-1))
print ("negatif =", (-x, -y))
